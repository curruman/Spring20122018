package Ejercicio1;

public class Numeros {
	private int n;
	private double a;
	private char c;
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	public double getA() {
		return a;
	}
	public void setA(double a) {
		this.a = a;
	}
	public char getC() {
		return c;
	}
	public void setC(char c) {
		this.c = c;
	}

}
